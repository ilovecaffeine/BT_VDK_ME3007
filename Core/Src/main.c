void MAX7219_Send(uint8_t reg, uint8_t data) {
    uint8_t txData[2] = {reg, data};
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET); // Set LOAD (CS) low
    HAL_SPI_Transmit(&hspi1, txData, 2, HAL_MAX_DELAY);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);   // Set LOAD (CS) high
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
}


void MAX7219_Init(void) {
    MAX7219_Send(MAX7219_REG_SCANLIMIT, 0x00);  // Display all 8 digits
    MAX7219_Send(MAX7219_REG_DECODEMODE, 0x00); // No decode mode
    MAX7219_Send(MAX7219_REG_SHUTDOWN, 0x01);   // Exit shutdown mode
    MAX7219_Send(MAX7219_REG_DISPLAYTEST, 0x00);// Disable display test
    MAX7219_Send(MAX7219_REG_INTENSITY, 0x0F);  // Set brightness to max
}

void MAX7219_SetDigit(uint8_t digit, uint8_t value) {
    if (digit < 8) {
        MAX7219_Send(MAX7219_REG_DIGIT0 + digit, value);
    }
}


